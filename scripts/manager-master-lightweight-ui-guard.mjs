#!/usr/bin/env node
import { existsSync, readFileSync } from 'node:fs';

const failures = [];
const read = (path) => existsSync(path) ? readFileSync(path, 'utf8') : (failures.push(`missing ${path}`), '');
const must = (source, marker, label) => { if (!source.includes(marker)) failures.push(label); };
const mustNot = (source, marker, label) => { if (source.includes(marker)) failures.push(label); };

const layout = read('app/layout.tsx');
const densityCss = read('app/account-record-density.css');
const shell = read('components/console/ConsoleShell.tsx');
const accountMenu = read('components/console/AccountMenu.tsx');
const debuggerMount = read('components/console/RenderDebuggerMount.tsx');
const instantLoading = read('components/console/ConsoleInstantLoading.tsx');
const admin = read('app/admin/page.tsx');
const adminAccess = read('app/admin/access/page.tsx');
const presenters = read('src/features/manager-console/admin-page-presenters.ts');
const masterHome = read('app/master/MasterConsoleHome.tsx');
const masterAccounts = read('app/master/accounts/page.tsx');
const masterTable = read('app/master/MasterAccountTableV2.tsx');
const assignBossRoute = read('app/api/master/assign-manager/route.ts');
const managerSettings = read('lib/saas/manager-user-settings.ts');
const managerKpis = read('src/features/manager-console/components/ManagerKpiGrid.tsx');
const masterKpis = read('src/features/master-console/components/MasterKpiGrid.tsx');
const panels = read('lib/manager-console/manager-operations-panels.ts');
const manifest = read('lib/console/contracts/navigation-manifest.ts');
const performance = read('src/features/performance/performance-contract.ts');

must(layout, "import './account-record-density.css';", 'root layout must load compact account record density CSS');
must(densityCss, '--account-record-density-contract: compact-records-click-for-details', 'account record density contract marker missing');
must(densityCss, '--xdisputer-currency-contract: php-peso', 'PHP currency contract marker missing');
must(densityCss, '--account-popover-close-contract: x-close-when-open', 'open metadata popovers must have an X close contract');
must(densityCss, '--account-metadata-layout-contract: status-note-salary-rate', 'manager metadata popover must keep status/note then salary/rate layout contract');
must(densityCss, '.manager-user-settings-details[open] > summary::after', 'metadata popover summary must become the X close control');
must(densityCss, 'content: "×"', 'metadata popover close control must render X');
must(densityCss, 'transform: translate(325px, -132px)', 'metadata X close control must be inside the popover top-right, not floating above the header');
must(densityCss, '.client-status-job-field', 'metadata popover must group client status/job description');
must(densityCss, '.manager-user-settings-notes', 'metadata popover must include note field');
must(densityCss, '.metadata-rule-hint', 'metadata popover must move explanatory copy out of the first row so note aligns with status');
must(densityCss, '.metadata-salary-field', 'metadata popover must include salary field');
must(densityCss, '.metadata-output-rate-field', 'metadata popover must include output per rate field');
must(densityCss, '.manager-user-settings-details[open] .manager-user-settings-form', 'metadata editor must become a popover when opened');
must(densityCss, 'position: fixed !important', 'metadata popover must avoid expanding the record row inline');
must(densityCss, '.account-control-trigger-grid', 'client/user trigger rows must have compact density styling');
must(densityCss, '.boss-assignment-form', 'boss assignment forms must inherit compact record styling');
must(shell, 'data-console-layout-ratio="75/25"', 'console shell must keep 75/25 layout contract');
must(shell, '<AccountMenu', 'console shell must delegate account rail to AccountMenu');
mustNot(shell, '<NotificationDock', 'console shell must not mount notification dock directly');
must(accountMenu, '<NotificationDock />', 'account rail must own notification dock');
must(debuggerMount, 'NEXT_PUBLIC_XDISPUTER_DEBUG_PANEL', 'debug panel must require explicit opt-in');
mustNot(debuggerMount, "process.env.NODE_ENV !== 'production') return true", 'debug panel must not auto-enable in development');
must(panels, "'output_activity'", 'manager operations must use output_activity id');
mustNot(panels, 'Access control of user', 'manager access nav label must not say Access control of user');
mustNot(admin, 'function PayrollPanel', 'manager admin page must not expose PayrollPanel');
must(admin, 'OutputActivityPanel', 'manager admin page must expose OutputActivityPanel');
must(admin, 'ManagerKpiGrid', 'manager admin page must use feature-owned KPI grid');
must(admin, 'accountsForPanel', 'manager admin page must route data by active panel');
must(admin, 'managerPanelHeader', 'manager admin page must use canonical panel header labels');
must(admin, "title: 'Access Control'", 'manager access panel header must be Access Control');
must(admin, 'data-compact-account-record="true"', 'manager account records must use compact record marker');
must(admin, '<details className="manager-user-settings-details">', 'manager metadata editor must be collapsed behind a native details control');
must(admin, 'Client status / job description', 'manager metadata editor must label client status/job description');
must(admin, '<option value="full_time">Full-time</option>', 'manager metadata editor must use Full-time label');
must(admin, '<option value="output_based">Per-output</option>', 'manager metadata editor must support per-output profile');
must(admin, 'className="manager-user-settings-notes"', 'manager metadata editor must include notes');
must(admin, 'className="metadata-rule-hint"', 'manager metadata editor must move explanatory copy below the first row');
must(admin, 'name="baseSalary"', 'manager metadata editor must save salary through baseSalary');
must(admin, 'name="perOutputRate"', 'manager metadata editor must save output per rate');
mustNot(admin, 'Manager owner', 'manager metadata editor must not expose manager owner in manager page popover');
must(admin, 'let pendingPromise', 'manager admin page must use lazy panel-specific data promises');
must(admin, "if (activePanel === 'monitoring')", 'manager monitoring panel must load only monitoring datasets');
must(admin, "activePanel === 'access' || activePanel === 'reports'", 'manager access/report panels must share one all-clients dataset');
must(admin, "activePanel === 'output_activity'", 'manager output activity must load only active accounts');
must(admin, 'needsUserSettings', 'manager user settings must only load for panels that render payroll metadata');
mustNot(admin, 'Access control of user', 'manager admin header must not say Access control of user');
mustNot(admin, 'ensureManagerInviteCode(supabase, user.id) : Promise.resolve', 'manager invite code must not be fetched for every panel');
mustNot(admin, 'const allAccounts = uniqueAccounts(pendingResult.accounts, activeResult.accounts, blockedResult.accounts)', 'manager admin page must not build all accounts from every status on every panel');
must(adminAccess, 'Access Control', 'manager access route must use Access Control label');
must(adminAccess, 'header={{ eyebrow:', 'manager access route must use shared ConsoleHeader');
mustNot(adminAccess, '<header className="admin-monitor-header native-command-hero', 'manager access route must not render a duplicate child hero header');
must(presenters, "currency: 'PHP'", 'manager money formatter must use PHP currency');
must(presenters, "new Intl.NumberFormat('en-PH'", 'manager money formatter must use Philippine locale');
mustNot(presenters, "currency: 'USD'", 'manager money formatter must not use USD');
must(managerSettings, 'MANAGER_USER_SETTING_COLUMNS', 'manager user settings query must define explicit column projection');
must(managerSettings, 'MANAGER_OUTPUT_APPROVAL_COLUMNS', 'manager output approvals query must define explicit column projection');
must(managerSettings, '.select(MANAGER_USER_SETTING_COLUMNS)', 'manager user settings must not select wildcard columns');
must(managerSettings, '.select(MANAGER_OUTPUT_APPROVAL_COLUMNS)', 'manager output approvals must not select wildcard columns');
mustNot(managerSettings, ".select('*')", 'manager metadata queries must not use wildcard select');
must(masterHome, 'MasterKpiGrid', 'master home must use feature-owned KPI grid');
must(masterHome, 'header={{ eyebrow:', 'master home must use shared ConsoleHeader instead of child hero header');
mustNot(masterHome, '<header className="admin-monitor-header native-command-hero', 'master home must not render duplicate child hero header');
must(masterAccounts, 'header={{ eyebrow:', 'master accounts must use shared ConsoleHeader instead of child hero header');
must(masterAccounts, 'selectedView === \'overview\' ? Promise.resolve', 'master accounts overview must avoid fetching a full directory table');
must(masterAccounts, 'bossOptionsFromManagers', 'master accounts must build boss manager options');
must(masterAccounts, 'bossOptions={bossOptions}', 'master accounts must pass boss options into account table');
mustNot(masterAccounts, '<header className="admin-monitor-header native-command-hero', 'master accounts must not render duplicate child hero header');
must(masterTable, 'BossAssignmentForm', 'master account table must expose boss assignment form');
must(masterTable, 'action="/api/master/assign-manager"', 'boss assignment must post to the master assignment route');
must(masterTable, 'name="managerId"', 'boss assignment form must submit manager id');
must(assignBossRoute, 'updateManagedAccount', 'boss assignment route must use central managed account mutation');
must(assignBossRoute, "nextManagerId: managerId", 'boss assignment route must update target client manager id');
must(instantLoading, 'data-console-header-grid="true"', 'console loading shell must inherit header grid layout');
must(instantLoading, 'manager-header-account-dock console-loading-account', 'console loading shell must include account rail skeleton');
must(instantLoading, 'data-console-header-primary="true"', 'console loading shell must use shared header marker');
must(managerKpis, 'manager-console-kpi-grid', 'manager KPI grid component must preserve premium card layout');
must(masterKpis, 'master-monitoring-stats', 'master KPI grid component must preserve master stats layout');
must(manifest, "'master-governance'", 'master governance manifest must stay wired through shared console shell');
must(performance, 'debugPanelDefault', 'performance contract must define debug panel default rule');

if (failures.length) {
  console.error(`manager-master-lightweight-ui-guard failed: ${failures.length} check(s).`);
  for (const failure of failures) console.error(`- ${failure}`);
  process.exit(1);
}

console.log('manager-master-lightweight-ui-guard: ok');
